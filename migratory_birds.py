def migratoryBirds(arr):
    freq = {}

    for bird in arr:
        freq[bird] = freq.get(bird, 0) + 1

    max_freq = max(freq.values())

    return min(bird for bird, count in freq.items() if count == max_freq)

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

